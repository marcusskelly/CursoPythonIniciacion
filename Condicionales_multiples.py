print("Convertidor de numeros a letras")

num_uno = int(input("Cual es el numero que te gustaria convertir?: "))

if num_uno == 1:
    print("El numero es 'Uno'")
elif num_uno == 2:
    print("El numero es 'Dos'")

elif num_uno == 3:
    print("El numero es 'Tres'")
    
elif num_uno == 4:
    print("El numero es 'Cuatro'")
    
elif num_uno == 5:
    print("El numero es '5'")
    
else:
    print("Este programa solo funciona hasta el 5")

print("Fin")
